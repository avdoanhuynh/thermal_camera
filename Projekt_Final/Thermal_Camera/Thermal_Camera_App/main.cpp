#include "window.h"
#include <QApplication>
#include <stdint.h>
#include "MLX90640_API.h"
#include <iostream>
#include <cstring>
#include <fstream>
#include <chrono>
#include <thread>
#include <math.h>

#include "opencv2/opencv.hpp"
#include "opencv2/imgproc/imgproc.hpp"
#include "opencv2/highgui/highgui.hpp"

using namespace cv;
using namespace std;

#define MLX_I2C_ADDR 0x33

// Valid frame rates are 1, 2, 4, 8, 16, 32 and 64
// The i2c baudrate is set to 1mhz to support these
#define FPS 8
#define FRAME_TIME_MICROS (1000000/FPS)

// Despite the framerate being ostensibly FPS hz
// The frame is often not ready in time
// This offset is added to the FRAME_TIME_MICROS
// to account for this.
#define OFFSET_MICROS 850

//Zoom function
//Source: https://stackoverflow.com/questions/14365411/opencv-crop-image/14365605
//Modified by <project_ge_wise2019_p2/Stud_4>
Mat zooming(Mat inp,int factor)
{
        Mat u8_mat_zoom;
        int x,y,w,h;
        if(factor == 1)
        {
            x = 0;
            y = 0;
        }
        else
        {
        x = (inp.cols/factor)/2;
        y = (inp.rows/factor)/2;
        }

        w = inp.cols / factor;
        h = inp.rows / factor;
        Rect roi = Rect(x,y,w,h);
        return u8_mat_zoom = inp(roi);
}

//Rotation function
//Source: https://stackoverflow.com/questions/22041699/rotate-an-image-without-cropping-in-opencv-in-c
//Modified by <project_ge_wise2019_p2/Stud_4>
Mat rotation(Mat inp, double winkel)
{
    Mat rotate;
    Point2f center(inp.cols/2., inp.rows/2);
    Mat r = getRotationMatrix2D(center,winkel,1.0);
    warpAffine(inp,rotate,r,Size(inp.cols,inp.rows));
    return rotate;
}


int main(int argc, char *argv[])
{
    QApplication app(argc, argv);
    Window main_window;
    main_window.show();

    //Variable for reading data from sensor
    static uint16_t eeMLX90640[832];
    float emissivity = 1;
    uint16_t frame[834];
    static float image[768];
    static float mlx90640To[768];
    float eTa;
    static uint16_t data[768*sizeof(float)];

    //Variable for display from thermal picture
    QImage thermal_pic;
    QImage thermal_pic_detection;

    //Varible for displaying Temperature on Click
    int point_x,point_y;
    float clickTemp, clickTempF;
     Mat size_mat_origin;

    //Variable for Edge detection
    int lowTh;
    int ratio =3;
    int kernel_size = 3;

    //Driver
    auto frame_time = std::chrono::microseconds(FRAME_TIME_MICROS + OFFSET_MICROS);

    MLX90640_SetDeviceMode(MLX_I2C_ADDR, 0);
    MLX90640_SetSubPageRepeat(MLX_I2C_ADDR, 0);
    switch(FPS)
    {
        case 1:
            MLX90640_SetRefreshRate(MLX_I2C_ADDR, 0b001);
            break;
        case 2:
            MLX90640_SetRefreshRate(MLX_I2C_ADDR, 0b010);
            break;
        case 4:
            MLX90640_SetRefreshRate(MLX_I2C_ADDR, 0b011);
            break;
        case 8:
            MLX90640_SetRefreshRate(MLX_I2C_ADDR, 0b100);
            break;
        case 16:
            MLX90640_SetRefreshRate(MLX_I2C_ADDR, 0b101);
            break;
        case 32:
            MLX90640_SetRefreshRate(MLX_I2C_ADDR, 0b110);
            break;
        case 64:
            MLX90640_SetRefreshRate(MLX_I2C_ADDR, 0b111);
            break;
        default:
            printf("Unsupported framerate: %d", FPS);
            return 1;
    }
    MLX90640_SetChessMode(MLX_I2C_ADDR);
    paramsMLX90640 mlx90640;
    MLX90640_DumpEE(MLX_I2C_ADDR, eeMLX90640);
    MLX90640_ExtractParameters(eeMLX90640, &mlx90640);


    while (1)
    {
        if(!main_window.freeze_check)       //Check if Freeze Button is pushed.
        {
            auto start = std::chrono::system_clock::now();
            MLX90640_GetFrameData(MLX_I2C_ADDR, frame);
            MLX90640_InterpolateOutliers(frame, eeMLX90640);
            eTa = MLX90640_GetTa(frame, &mlx90640);
            MLX90640_CalculateTo(frame, &mlx90640, emissivity, eTa, mlx90640To);

            // //////////////////////////////////////////////////////////////////////////
            //Original Image (without processing) --> To take information from to display
            // //////////////////////////////////////////////////////////////////////////
            //Convert from Array (sensor) to Array (openCV)
            Mat IR_mat_origin (32,24, CV_32FC1, data);
            for(int y = 0; y < 24; y++)
            {
                for(int x = 0; x < 32; x++)
                {
                    float val = mlx90640To[32 * (23-y) + x];
                    IR_mat_origin.at<float>(x,y) = val;
                }
            }

            //Display max and min temperature
            double minTemp, maxTemp;
            minMaxLoc(IR_mat_origin, &minTemp, &maxTemp);
            if(main_window.conversion_c->isChecked()==true)     //Check if unit is Celcius or Fahrenheit
            {
                main_window.frame_min->setText("Min:\n"+QString::number(minTemp)+"\xB0 C");
                main_window.frame_max->setText("Max:\n"+QString::number(maxTemp)+"\xB0 C");
            }
            else
            {
                minTemp=(minTemp*9.0/5.0)+32;
                maxTemp=(maxTemp*9.0/5.0)+32;
                main_window.frame_min->setText("Min:\n"+QString::number(minTemp)+"\xB0 F");
                main_window.frame_max->setText("Max:\n"+QString::number(maxTemp)+"\xB0 F");
            }

            //Display temperature on click
            resize(IR_mat_origin, size_mat_origin, Size(336,448), INTER_CUBIC); //Size(cols,rows)
            cv::rotate(size_mat_origin,size_mat_origin,cv::ROTATE_90_CLOCKWISE);
            cv::flip(size_mat_origin,size_mat_origin,+1);

            //Take coordinate from mouse, with adjustment since coordinators from image and Qlabel are different
            point_x = main_window.Coordinate.x()-1; //Border from QLable has size of 1 px --> Adjusment: -1
            point_y = main_window.Coordinate.y()-1;

            //Display temperature from the clicked pixel
            if(point_x>=0 && point_x<=448 && point_y>=0 && point_y<=336)
            {
                if (main_window.conversion_c->isChecked()==true)
                {
                    clickTemp = size_mat_origin.at<float>(point_y,point_x);
                    main_window.frame_cursor->setText("Temp:\n"+QString::number(clickTemp)+"\xB0 C");
                }
                else
                {
                    clickTemp = size_mat_origin.at<float>(point_y,point_x);
                    clickTempF = (clickTemp * 9.0/5.0)+32;
                    main_window.frame_cursor->setText("Temp:\n"+QString::number(clickTempF)+"\xB0 F");
                }

            }

            // /////////////////////////////////////////////////////////////////////////////////////////
            // Image Processing and Displaying
            // /////////////////////////////////////////////////////////////////////////////////////////

            //Take temperature range from UI
            Mat IR_mat (32,24, CV_32FC1, data);
            int r_max,r_min;
            r_max = main_window.range_max->value();
            r_min = main_window.range_min->value();
            if(!main_window.conversion_c->isChecked()==true)
            {
                r_max = (r_max-32)*5/9;
                r_min = (r_min-32)*5/9;

            }

            //Convert from Array (sensor) to Array (OpenCV) with consider from temperature range
            for(int y = 0; y < 24; y++)
            {
                for(int x = 0; x < 32; x++)
                {
                    float val2 = mlx90640To[32 * (23-y) + x];
                    if(val2>r_max)
                    {
                        val2=static_cast<float>(r_max);
                    }
                    else if (val2<r_min)
                    {
                        val2=static_cast<float>(r_min);
                    }
                    IR_mat.at<float>(x,y) = val2;
                }
            }

            // Normalize the mat (with consider the temperature range)
            Mat normal_mat (32,24, CV_32FC1, data);
            for(int y = 0; y < 24; y++)
            {
                for(int x = 0; x < 32; x++)
                {
                    float val3 = (IR_mat.at<float>(x,y)-static_cast<float>(r_min))/(static_cast<float>(r_max)-static_cast<float>(r_min));
                    normal_mat.at<float>(x,y) = val3;
                }
            }

            // Convert Mat to CV_U8 to use applyColorMap
            Mat u8_mat;
            normal_mat.convertTo(u8_mat, CV_8U, 255.0/(1.0 - 0), -0);

            //Zoom
            Mat u8_mat_zoom;
            u8_mat_zoom = zooming(u8_mat,main_window.factor_zoom);

            //Rotation
            Mat rotate;
            rotate = rotation(u8_mat_zoom,main_window.factor_rotation);

            // Resize mat
            Mat size_mat;
            resize(rotate, size_mat, Size(336,448), INTER_CUBIC);
            cv::rotate(size_mat,size_mat,cv::ROTATE_90_CLOCKWISE);
            cv::flip(size_mat,size_mat,+1);

            //Gaussian_Filter --> Image Smoothing
            Mat gaussianFilter;
            GaussianBlur(size_mat,gaussianFilter,Size(),3,3,0);

            // Apply false color
            Mat falsecolor_mat;
            applyColorMap(gaussianFilter, falsecolor_mat, COLORMAP_JET);

            // ///////////////////////////////////////////////////////////////////////////////////////////////////////
            // Edge detection
            // Source: https://docs.opencv.org/2.4/doc/tutorials/imgproc/imgtrans/canny_detector/canny_detector.html
            // Modified by <project_ge_wise2019_p2/Stud_4>
            // /////////////////////////////////////////////////////////////////////////////////////////////////////

            if(main_window.detection_check)     //Check if Edge Detection mode is on
            {
                //Convert to gray scale
                Mat grayscale;
                cvtColor(falsecolor_mat, grayscale, CV_BGR2GRAY);

                //Gaussian Blur
                Mat grayscale_blur;
                GaussianBlur(grayscale,grayscale_blur,Size(),3,3,0);

                // Canny Edge
                Mat edge_detection;
                lowTh = main_window. frame_detection_slider->value();   //Take threshold value from UI
                Canny(grayscale_blur, edge_detection, lowTh, lowTh*ratio, kernel_size);

                //Convert from OpenCV Image to QImage
                thermal_pic_detection = QImage((const unsigned char*)(edge_detection.data),edge_detection.cols,edge_detection.rows,edge_detection.step,QImage::Format_Indexed8);

                //Display image after edge detection
                main_window.thermal_display->setPixmap(QPixmap::fromImage(thermal_pic_detection));
            }
            else
            {
                //Edge Detection mode is off --> Display normal thermal image
                //Convert from OpenCV Image to QImage
                cvtColor(falsecolor_mat,falsecolor_mat,CV_BGR2RGB); //Image in openCV is BGR, QImage is RGB --> need to convert from BGR to RGB
                thermal_pic = QImage((const unsigned char*)(falsecolor_mat.data),falsecolor_mat.cols,falsecolor_mat.rows,falsecolor_mat.step,QImage::Format_RGB888);

                //Display thermal image
                main_window.thermal_display->setPixmap(QPixmap::fromImage(thermal_pic));
            }

            auto end = std::chrono::system_clock::now();
            auto elapsed = std::chrono::duration_cast<std::chrono::microseconds>(end - start);
            std::this_thread::sleep_for(std::chrono::microseconds(frame_time - elapsed));
        }

        else
        {
            //Display temperature on click when Freeze mode is on
            point_x = main_window.Coordinate.x()-1;
            point_y = main_window.Coordinate.y()-1;

            if(point_x>=0 && point_x<=448 && point_y>=0 && point_y<=336)
            {
                if (main_window.conversion_c->isChecked()==true)
                {
                    clickTemp = size_mat_origin.at<float>(point_y,point_x);
                    main_window.frame_cursor->setText("Temp:\n"+QString::number(clickTemp)+"\xB0 C");
                }
                else
                {
                    clickTemp = size_mat_origin.at<float>(point_y,point_x);
                    clickTempF = (clickTemp * 9.0/5.0)+32;
                    main_window.frame_cursor->setText("Temp:\n"+QString::number(clickTempF)+"\xB0 F");
                }
            }
        }

        //Since we use infinite loop --> have to call this function so that Qt can process new event --> better methode: Thread
        QCoreApplication::processEvents();
    }

    return app.exec();

}
